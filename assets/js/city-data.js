/** Shared comment renderer for news pages */
export function renderComments(articleId, mountEl) {
  if (!mountEl) return;
  const list = COMMENTS[articleId] || [];
  const items = list
    .map(
      (c) => `
    <li class="comment-item">
      <div class="comment-user">${escapeHtml(c.user)}<span class="time">${escapeHtml(c.time || '')}</span></div>
      <div class="comment-text">${escapeHtml(c.text)}</div>
    </li>`
    )
    .join('');

  mountEl.innerHTML = `
    <div class="comments-head">
      网友留言
      <span class="count">共 ${list.length} 条</span>
    </div>
    <ul class="comment-list">${items || '<li class="comment-item"><div class="comment-text">暂无留言</div></li>'}</ul>
    <div class="comment-form-fake">
      登录后可留言（本站演示环境暂不开放发帖）
      <input type="text" disabled placeholder="说点什么…" aria-hidden="true" />
    </div>
  `;
}

function escapeHtml(s) {
  return String(s ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}
